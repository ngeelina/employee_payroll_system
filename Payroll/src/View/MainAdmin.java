    public void edit(){
        String emp_ID = JOptionPane.showInputDialog(null, "Enter emp_ID to edit");
        if(emp_ID == null){   
        }
        else{
            ec.getEmployees().setEmp_ID(emp_ID);
            if(ec.find() == 1){
                EditEmployee editEmployee = new EditEmployee(this,true,emp_ID);
                editEmployee.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Wrong emp_ID","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }
    
    public void delete(){
        String emp_ID = JOptionPane.showInputDialog(null, "Enter emp_ID to delete");
        if(emp_ID == null){   
        }
        else{
            ec.getEmployees().setEmp_ID(emp_ID);
            if(ec.find() == 1){
                int reply = JOptionPane.showConfirmDialog(null,"Confirm Delete?","Delete employee",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
                if (reply == JOptionPane.YES_OPTION){
                    int i = ec.delete();
                    if(i == 0){
                        JOptionPane.showMessageDialog(null, "This user can't be deleted","Error",JOptionPane.ERROR_MESSAGE);
                    }
                    else if(i == 1){
                        JOptionPane.showMessageDialog(null, "Employee deleted","Success",JOptionPane.PLAIN_MESSAGE);
                        dispose();
                        EmployeeDetails ed = new EmployeeDetails(null, true);
                        ed.setVisible(true);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Deletion error","Error",JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Wrong emp_ID","Error",JOptionPane.ERROR_MESSAGE);
            }
        } 
    }
    
