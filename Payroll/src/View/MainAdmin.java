    public void run(String exe){
        Runtime rt = Runtime.getRuntime();
        try{
            rt.exec(exe);
        }
        catch(IOException ex){
            JOptionPane.showMessageDialog(null,ex.getMessage(),"Error Found",JOptionPane.ERROR_MESSAGE);
        }
    }
    
